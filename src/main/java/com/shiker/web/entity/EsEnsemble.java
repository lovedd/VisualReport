package com.shiker.web.entity;

public class EsEnsemble {
	private String cdate;
	private long totalCnt;
	private long hitRuleCnt;
	private long reChargeCnt;

	public String getCdate() {
		return cdate;
	}
	public void setCdate(String cdate) {
		this.cdate = cdate;
	}
	public long getTotalCnt() {
		return totalCnt;
	}
	public void setTotalCnt(long totalCnt) {
		this.totalCnt = totalCnt;
	}
	public long getHitRuleCnt() {
		return hitRuleCnt;
	}
	public void setHitRuleCnt(long hitRuleCnt) {
		this.hitRuleCnt = hitRuleCnt;
	}
	public long getReChargeCnt() {
		return reChargeCnt;
	}
	public void setReChargeCnt(long reChargeCnt) {
		this.reChargeCnt = reChargeCnt;
	}
	
}
