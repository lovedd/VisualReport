package com.shiker.web.entity;

public class ESRiskDev {
	private String cdate;
	private String devFp;
	private double scores;

	public String getCdate() {
		return cdate;
	}

	public void setCdate(String cdate) {
		this.cdate = cdate;
	}

	public double getScores() {
		return scores;
	}

	public void setScores(double scores) {
		this.scores = scores;
	}

	public String getDevFp() {
		return devFp;
	}

	public void setDevFp(String devFp) {
		this.devFp = devFp;
	}
}
