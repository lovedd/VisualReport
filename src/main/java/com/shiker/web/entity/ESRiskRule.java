package com.shiker.web.entity;

public class ESRiskRule {
	private String cdate;
	private long num;
	private String ruleRiskTip;

	public String getCdate() {
		return cdate;
	}

	public void setCdate(String cdate) {
		this.cdate = cdate;
	}

	public String getRuleRiskTip() {
		return ruleRiskTip;
	}

	public void setRuleRiskTip(String ruleRiskTip) {
		this.ruleRiskTip = ruleRiskTip;
	}

	public long getNum() {
		return num;
	}

	public void setNum(long num) {
		this.num = num;
	}

}
